class MnArray < PrintableArray
  def elem(v)
    sprintf("{%d, %d}", v[0], v[1])
  end
end

def INITIAL_VALUE.all_of_length_are_same
  1 == Array.new(self.length) {|i|
    self[i].flatten.length
  }.uniq.length
end

